import { CstParser, createToken, Lexer } from 'chevrotain';

/**
 * ---------------------------------------
 * PHASE 1: THE LEXER (TOKEN DEFINITIONS)
 * -----------------------------------------
 * In Chevrotain, the input string must first be converted into an array of Tokens.
 * This is where the primary performance bottleneck occurs compared to a hand-written parser,
 * as every character must be evaluated against multiple Regular Expressions.
 */

// WARNING: Token order is critical in Chevrotain. 
// The Lexer evaluates tokens top-to-bottom. 
// Longer overlapping matches (like **) MUST be defined before shorter ones (*),
// otherwise the Lexer will consume the first '*' and break the double asterisk logic.
const DoubleAsterisk = createToken({ name: 'DoubleAsterisk', pattern: /\*\*/ });
const Asterisk = createToken({ name: 'Asterisk', pattern: /\*/ });
const DoubleUnderscore = createToken({ name: 'DoubleUnderscore', pattern: /__/ });
const Underscore = createToken({ name: 'Underscore', pattern: /_/ });
const DoubleTilde = createToken({ name: 'DoubleTilde', pattern: /~~/ });
const Tilde = createToken({ name: 'Tilde', pattern: /~/ });
const Backtick = createToken({ name: 'Backtick', pattern: /`/ });

/** * Simple regex for mentions to match the handwritten parser's logic.
 * Note: Regex engine overhead applies here on every match.
 */
const Mention = createToken({ name: 'Mention', pattern: /[@#][a-zA-Z0-9.-]+/ });

/** * The Fallback Token.
 * Text is defined as anything that IS NOT one of our special delimiter characters.
 * This acts similarly to the stopAt array in our handwritten parsePlainText function.
 */
const Text = createToken({ name: 'Text', pattern: /[^*_~`@#]+/ });

// Register tokens in the exact order the Lexer should attempt to match them.
const allTokens = [
    DoubleAsterisk, Asterisk,
    DoubleUnderscore, Underscore,
    DoubleTilde, Tilde,
    Backtick,
    Mention,
    Text
];

// Initialize the Lexer
const MessageLexer = new Lexer(allTokens);

/**
 * ----------------------------------------
 * PHASE 2: THE PARSER (GRAMMAR RULES)
 * --------------------------------------
 * The CstParser consumes the token stream generated by the Lexer and outputs
 * a Concrete Syntax Tree (CST). 
 */
class MessageParser extends CstParser {
    constructor() {
        super(allTokens);
        // This validates the grammar rules at initialization to prevent infinite loops
        this.performSelfAnalysis();
    }

    /**
     * Main entry point. 
     * A valid message consists of zero or more inline items.
     */
    public message = this.RULE('message', () => {
        this.MANY(() => {
            this.SUBRULE(this.inlineItem);
        });
    });

    /**
     * The Orchestrator Rule.
     * Functions identically to parseInlineContent in the hand-written parser.
     * It attempts to match complex formatting/mentions before falling back to plain text.
     */
    private inlineItem = this.RULE('inlineItem', () => {
        this.OR([
            { ALT: () => this.SUBRULE(this.bold) },
            { ALT: () => this.SUBRULE(this.italic) },
            { ALT: () => this.SUBRULE(this.strike) },
            { ALT: () => this.SUBRULE(this.code) },
            { ALT: () => this.CONSUME(Mention) },
            { ALT: () => this.CONSUME(Text) }
        ]);
    });

    /**
     * Parses Bold markup.
     * Note the use of CONSUME2 and SUBRULE2: Chevrotain requires unique numerical 
     * suffixes when the same rule is called multiple times within the same OR block 
     * to track the parsing state correctly.
     */
    private bold = this.RULE('bold', () => {
        this.OR([
            {
                ALT: () => {
                    this.CONSUME(DoubleAsterisk);
                    this.MANY(() => this.SUBRULE(this.inlineItem)); // Allow nested items inside **
                    this.CONSUME2(DoubleAsterisk);
                }
            },
            {
                ALT: () => {
                    this.CONSUME(Asterisk);
                    this.MANY2(() => this.SUBRULE2(this.inlineItem)); // Allow nested items inside *
                    this.CONSUME2(Asterisk);
                }
            }
        ]);
    });

    /**
     * Parses Italic markup.
     */
    private italic = this.RULE('italic', () => {
        this.OR([
            {
                ALT: () => {
                    this.CONSUME(DoubleUnderscore);
                    this.MANY(() => this.SUBRULE(this.inlineItem));
                    this.CONSUME2(DoubleUnderscore);
                }
            },
            {
                ALT: () => {
                    this.CONSUME(Underscore);
                    this.MANY2(() => this.SUBRULE2(this.inlineItem));
                    this.CONSUME2(Underscore);
                }
            }
        ]);
    });

    /**
     * Parses Strikethrough markup.
     */
    private strike = this.RULE('strike', () => {
        this.OR([
            {
                ALT: () => {
                    this.CONSUME(DoubleTilde);
                    this.MANY(() => this.SUBRULE(this.inlineItem));
                    this.CONSUME2(DoubleTilde);
                }
            },
            {
                ALT: () => {
                    this.CONSUME(Tilde);
                    this.MANY2(() => this.SUBRULE2(this.inlineItem));
                    this.CONSUME2(Tilde);
                }
            }
        ]);
    });

    /**
     * Parses Inline Code markup.
     * Bypasses the inlineItem rule entirely and only consumes raw Text tokens,
     * protecting code blocks from accidental markup/mention formatting.
     */
    private code = this.RULE('code', () => {
        this.CONSUME(Backtick);
        this.MANY(() => this.CONSUME(Text)); 
        this.CONSUME2(Backtick);
    });
}

// Instantiate the parser once to avoid recreation overhead during benchmarking
const parser = new MessageParser();

/**
 * --------------------------------
 * EXPORTED BENCHMARK FUNCTION
 * -------------------------------
 * @param input The raw message string
 * @returns The Concrete Syntax Tree (CST) or null if parsing fails
 */
export function parseChevrotain(input: string) {
    // Step 1: Lexing (String -> Tokens)
    const lexResult = MessageLexer.tokenize(input);
    
    // Step 2: Parsing (Tokens -> CST)
    parser.input = lexResult.tokens;
    const cst = parser.message();

    // Safe backtracking representation for the benchmark:
    // If Chevrotain hits an unclosed tag (like *hello), it registers an error.
    // We return null to match the failure state of our hand-written parser.
    if (parser.errors.length > 0) {
        return null; 
    }

    return cst;
}